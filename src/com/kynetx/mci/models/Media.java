package com.kynetx.mci.models;

public class Media {
	
	private String mediaGuid;
	private String mediaCoverArt;
	private String mediaType;
	private String mediaUrl;
	private String mediaTitle;
	private String mediaDescription;
	/**
	 * @return the mediaGuid
	 */
	public String getMediaGuid() {
		return mediaGuid;
	}
	/**
	 * @param mediaGuid the mediaGuid to set
	 */
	public void setMediaGuid(String mediaGuid) {
		this.mediaGuid = mediaGuid;
	}
	/**
	 * @return the mediaCoverArt
	 */
	public String getMediaCoverArt() {
		return mediaCoverArt;
	}
	/**
	 * @param mediaCoverArt the mediaCoverArt to set
	 */
	public void setMediaCoverArt(String mediaCoverArt) {
		this.mediaCoverArt = mediaCoverArt;
	}
	/**
	 * @return the mediaType
	 */
	public String getMediaType() {
		return mediaType;
	}
	/**
	 * @param mediaType the mediaType to set
	 */
	public void setMediaType(String mediaType) {
		this.mediaType = mediaType;
	}
	/**
	 * @return the mediaUrl
	 */
	public String getMediaUrl() {
		return mediaUrl;
	}
	/**
	 * @param mediaUrl the mediaUrl to set
	 */
	public void setMediaUrl(String mediaUrl) {
		this.mediaUrl = mediaUrl;
	}
	/**
	 * @return the mediaTitle
	 */
	public String getMediaTitle() {
		return mediaTitle;
	}
	/**
	 * @param mediaTitle the mediaTitle to set
	 */
	public void setMediaTitle(String mediaTitle) {
		this.mediaTitle = mediaTitle;
	}
	/**
	 * @return the mediaDescription
	 */
	public String getMediaDescription() {
		return mediaDescription;
	}
	/**
	 * @param mediaDescription the mediaDescription to set
	 */
	public void setMediaDescription(String mediaDescription) {
		this.mediaDescription = mediaDescription;
	}
	

}
